package misClases;

public class Coordenada {

	private double compX;
	private double compY;
	private double compZ;

	public Coordenada(double compX, double compY, double compZ) {
		this.compX = compX;
		this.compY = compY;
		this.compZ = compZ;
	}

	public double getCompX() {
		return compX;
	}

	public void setCompX(double compX) {
		this.compX = compX;
	}

	public double getCompY() {
		return compY;
	}

	public void setCompY(double compY) {
		this.compY = compY;
	}

	public double getCompZ() {
		return compZ;
	}

	public void setCompZ(double compZ) {
		this.compZ = compZ;
	}
}
